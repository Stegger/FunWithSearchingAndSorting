    private void btnTestSortingActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnTestSortingActionPerformed
    {//GEN-HEADEREND:event_btnTestSortingActionPerformed
        SwingUtilities.invokeLater(new Runnable()
        {

            @Override
            public void run()
            {
                JFrame frame = new JFrame("Testing Sorting Algorithms");
                frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                boolean reversed = btnRadioReversed.isSelected();
                boolean sorted = btnRadioSorted.isSelected() || btnRadioReversed.isSelected();
                SortTestPanel testPanel = new SortTestPanel(searchBase, sorted, reversed);
                frame.add(testPanel);
                frame.pack();
                frame.setResizable(false);
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
            }
        });
    }//GEN-LAST:event_btnTestSortingActionPerformed

