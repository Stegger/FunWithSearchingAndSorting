    private void btnCreateArrayActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnCreateArrayActionPerformed
    {//GEN-HEADEREND:event_btnCreateArrayActionPerformed
        int size = Integer.parseInt(txtListSize.getText());
        int minVal = Integer.parseInt(txtMinVal.getText());
        int maxVal = Integer.parseInt(txtMaxVal.getText());

        if (btnRadioUnsorted.isSelected())
        {
            searchBase = ArrayFactory.fillArray(size, minVal, maxVal);
            txtSortTime.setText("NaN");
        }
        else if (btnRadioReversed.isSelected())
        {
            timer.reset();
            timer.start();
            searchBase = ArrayFactory.fillSortedArray(size, minVal, maxVal);
            arraySorter.reverse(searchBase);
            timer.stop();
            txtSortTime.setText(timer.getSeconds() + "");
        }
        else
        {
            searchBase = ArrayFactory.fillArray(size, minVal, maxVal);
            doMySortingAlgorithms();
        }

        int firstIndex = searchBase[0];
        int middleIndex = searchBase[searchBase.length / 2];
        int lastIndex = searchBase[searchBase.length - 1];
        txtFirstIndex.setText(firstIndex + "");
        txtMiddleIndex.setText(middleIndex + "");
        txtLastIndex.setText(lastIndex + "");

        btnTestSorting.setEnabled(true);
        comboSearchType.setEnabled(true);
        txtSearchValue.setEnabled(true);
        btnSearch.setEnabled(true);
    }//GEN-LAST:event_btnCreateArrayActionPerformed

    private void doMySortingAlgorithms()
    {
        timer.reset();
        timer.start();
        switch (comboSortAlgo.getSelectedIndex())
        {
            case 0:
                arraySorter.javaSort(searchBase);
                break;
            case 1:
                arraySorter.bubbleSort(searchBase);
                break;
            case 2:
                arraySorter.selectionSort(searchBase);
                break;
            case 3:
                arraySorter.insertionSort(searchBase);
                break;
            case 4:
                arraySorter.heapSort(searchBase);
                break;
            case 5:
                arraySorter.quicksort(searchBase);
                break;
            case 6:
                arraySorter.mergeSort(searchBase);
                break;
        }
        timer.stop();
        txtSortTime.setText(timer.getSeconds() + "");
    }

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSearchActionPerformed
    {//GEN-HEADEREND:event_btnSearchActionPerformed
        int selection = comboSearchType.getSelectedIndex();
        int searchVal = Integer.parseInt(txtSearchValue.getText());
        int index = -1;
        timer.reset();
        timer.start();
        switch (selection)
        {
            case 0:
                index = searchLogic.findFirst(searchVal, searchBase);
                break;
            case 1:
                index = searchLogic.findLast(searchVal, searchBase);
                break;
            case 2:
                index = searchLogic.binSearch(searchVal, searchBase);
                break;
            case 3:
                index = searchLogic.binFindFirst(searchVal, searchBase);
                break;
            case 4:
                index = searchLogic.binFindLast(searchVal, searchBase);
                break;
            default:
                break;
        }
        timer.stop();
        txtSearchResult.setText(index + "");
        txtSearchTime.setText(timer.getSeconds() + "");
    }//GEN-LAST:event_btnSearchActionPerformed

